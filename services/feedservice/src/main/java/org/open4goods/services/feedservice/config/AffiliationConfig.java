package org.open4goods.services.feedservice.config;

public class AffiliationConfig {
	
	private String awinAdvertiserId;
	
	private String awinAccessToken; 
	
	private String effiliationApiKey;

	private String tradetrackerCustomerId;
	private String tradetrackerApiKey;

	private String kwankoToken;

	private String webgainsApiKey;

	public String getAwinAdvertiserId() {
		return awinAdvertiserId;
	}

	public void setAwinAdvertiserId(String awinAdvertiserId) {
		this.awinAdvertiserId = awinAdvertiserId;
	}

	public String getAwinAccessToken() {
		return awinAccessToken;
	}

	public void setAwinAccessToken(String awinAccessToken) {
		this.awinAccessToken = awinAccessToken;
	}

	public String getEffiliationApiKey() {
		return effiliationApiKey;
	}

	public void setEffiliationApiKey(String effiliationApiKey) {
		this.effiliationApiKey = effiliationApiKey;
	}

	public String getTradetrackerCustomerId() {
		return tradetrackerCustomerId;
	}

	public void setTradetrackerCustomerId(String tradetrackerCustomerId) {
		this.tradetrackerCustomerId = tradetrackerCustomerId;
	}

	public String getTradetrackerApiKey() {
		return tradetrackerApiKey;
	}

	public void setTradetrackerApiKey(String tradetrackerApiKey) {
		this.tradetrackerApiKey = tradetrackerApiKey;
	}

	public String getKwankoToken() {
		return kwankoToken;
	}

	public void setKwankoToken(String kwankoToken) {
		this.kwankoToken = kwankoToken;
	}

	public String getWebgainsApiKey() {
		return webgainsApiKey;
	}

	public void setWebgainsApiKey(String webgainsApiKey) {
		this.webgainsApiKey = webgainsApiKey;
	}
}
