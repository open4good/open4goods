package org.open4goods.config.yml.ui;

public class ApiConfig {


	private String apiTitle;

	private String apiDescription;

	private String apiVersion;

	private String apiLicence;

	private String apiLicenceUrl;

	private String apiTermsOfServiceUrl;

	public String getApiTitle() {
		return apiTitle;
	}

	public void setApiTitle(String apiTitle) {
		this.apiTitle = apiTitle;
	}

	public String getApiDescription() {
		return apiDescription;
	}

	public void setApiDescription(String apiDescription) {
		this.apiDescription = apiDescription;
	}

	public String getApiVersion() {
		return apiVersion;
	}

	public void setApiVersion(String apiVersion) {
		this.apiVersion = apiVersion;
	}

	public String getApiLicence() {
		return apiLicence;
	}

	public void setApiLicence(String apiLicence) {
		this.apiLicence = apiLicence;
	}

	public String getApiLicenceUrl() {
		return apiLicenceUrl;
	}

	public void setApiLicenceUrl(String apiLicenceUrl) {
		this.apiLicenceUrl = apiLicenceUrl;
	}

	public String getApiTermsOfServiceUrl() {
		return apiTermsOfServiceUrl;
	}

	public void setApiTermsOfServiceUrl(String apiTermsOfServiceUrl) {
		this.apiTermsOfServiceUrl = apiTermsOfServiceUrl;
	}
	
	
	
	

}
