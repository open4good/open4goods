package org.open4goods.api.services.feed;

public class AffiliationConfig {
	
	private String awinAdvertiserId;
	
	private String awinAccessToken; 
	
	private String effiliationApiKey;

	public String getAwinAdvertiserId() {
		return awinAdvertiserId;
	}

	public void setAwinAdvertiserId(String awinAdvertiserId) {
		this.awinAdvertiserId = awinAdvertiserId;
	}

	public String getAwinAccessToken() {
		return awinAccessToken;
	}

	public void setAwinAccessToken(String awinAccessToken) {
		this.awinAccessToken = awinAccessToken;
	}

	public String getEffiliationApiKey() {
		return effiliationApiKey;
	}

	public void setEffiliationApiKey(String effiliationApiKey) {
		this.effiliationApiKey = effiliationApiKey;
	}
	
	

}
